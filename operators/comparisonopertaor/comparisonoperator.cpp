#include<iostream>
using namespace std;
int main(){
    int a=10,b=5;
    if(a==b){
        cout<<"a is equal to b";
    }
    else if(a!=b){
        cout<<"a is not equal to b";
    }
    else if(a>b){
        cout<<"a is greater than b";
    }
    else if(a<b){
        cout<<"a is less than b";
    }
    return 0;
}